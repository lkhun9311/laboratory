# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   문자열 s의 각 단어에서, 짝수번째 글자는 대문자 / 홀수번째 글자는 소문자로 바꾼다.
#   - 인덱스는 '단어별로' 0부터 다시 센다 (단어 경계에서 리셋).
#   - 단어 사이 공백은 그대로 유지하며, 연속 공백도 보존한다.
#   - 예) "try hello world" → "TrY HeLlO WoRlD"

# ─────────────────────────────────────────────────────────────
# 풀이 1: 단어별 enumerate로 변환 후 join
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   s.split(" ")로 공백 기준 분리 → 인자 있는 split이라 연속 공백이 보존된다.
#     (인자 없는 .split()은 연속 공백을 합쳐버려 오답이 되므로 주의)
#   각 단어를 enumerate로 돌며 인덱스 짝/홀로 대소문자를 결정 →
#     단어마다 인덱스가 0부터 시작하므로 "단어별 리셋"이 자연히 처리된다.
#   range(len())로 인덱싱하지 않고 글자(w)를 직접 순회해 가독성을 높였다.
def solution(s):
    result = []
    for word in s.split(" "):
        result.append("".join(w.upper() if i % 2 == 0 else w.lower() for i, w in enumerate(word)))
    return " ".join(result)

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 문자열 길이가 L일 때,
# 시간: O(L) — 모든 글자를 한 번씩 변환하는 게 하한
# 공간: O(L) — 단어 리스트와 결과 문자열 크기
# 결론: 글자를 한 번씩 훑는 게 목적이라 더 줄일 여지 없는 최적 복잡도.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. split(" ")(인자 있음)와 .split()(인자 없음)은 다르다 →
#      연속/끝 공백을 보존해야 하면 반드시 구분자를 명시해야 한다.
#   2. "단어별 인덱스 리셋"은 단어 단위로 분리해 각각 enumerate하면 공짜로 얻는다.
#   3. range(len(x))[i] 인덱싱보다 enumerate로 (인덱스, 원소)를 직접 받는 게
#      더 읽기 쉽고 실수도 줄인다 — 짧게가 아니라 '읽기 쉽게' 위해서도 그렇다.
#   4. 가독성은 "한 줄로 압축"이 아니라 "흐름이 눈에 보이는가"로 판단한다.

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    ("try hello world", "TrY HeLlO WoRlD"),
    ("try   hello", "TrY   HeLlO"),  # 연속 공백 보존 확인
    ("try ", "TrY "),                # 끝 공백 보존 확인
    ("t", "T"),
    ("", ""),
]

run_tests([solution], test_cases)