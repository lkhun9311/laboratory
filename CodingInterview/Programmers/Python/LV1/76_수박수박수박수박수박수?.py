# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   자연수 n을 입력받아 "수박수박수박..." 패턴을 길이 n만큼 반환한다.
#   - n이 짝수면 "수박"이 딱 떨어지고 (n=4 → "수박수박")
#   - n이 홀수면 마지막에 "수"로 끝난다 (n=3 → "수박수")
#   제약: 1 ≤ n ≤ 10000  → 길이가 작아 성능은 신경 쓸 필요 없음(O(n)이면 충분)

# ─────────────────────────────────────────────────────────────
# 풀이 1: 짝/홀 분기 (처음 접근)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   "수박"(2글자) 묶음이 몇 번 들어가는지(share)와
#   홀수일 때 남는 "수" 하나(rem)를 나눠서 직접 조립한다.
#   → 직관적이지만 짝수/홀수 경계를 손으로 처리해야 한다.
def solution1(n):
    share = n // 2                         # "수박" 묶음 개수
    rem = n % 2                            # 홀수면 1(남는 "수"), 짝수면 0
    return ("수박" * share) + ("수" * rem)


# ─────────────────────────────────────────────────────────────
# 풀이 2: 넉넉히 만들고 잘라내기 (슬라이싱)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   "수박"을 n번 반복하면 길이가 2n → 항상 n 이상이 보장된다.
#   거기서 앞 n글자만 잘라내면 패턴이 자연스럽게 끊긴다.
#   → 짝/홀 분기가 통째로 사라져 한 줄로 끝난다.
def solution2(n):
    return ("수박" * n)[:n]


# ─────────────────────────────────────────────────────────────
# 풀이 3: 반복 횟수 최소화 (메모리 절감판)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   풀이 2는 잠깐 길이 2n짜리 문자열을 만든다(절반은 버려짐).
#   필요한 "수박" 묶음 수는 올림(n/2) = (n+1)//2 개면 충분하므로
#   딱 그만큼만 반복해 임시 문자열을 절반으로 줄인다.
#   (n=10000 제약에선 불필요한 최적화지만, 입력이 컸다면 의미 있음)
def solution(n):
    repeat_n = (n + 1) // 2                # 필요한 "수박" 묶음 수 = ⌈n/2⌉
    return ("수박" * repeat_n)[:n]


# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 시간: 세 풀이 모두 O(n) — 길이 n 문자열을 한 번 만드는 게 하한
# 공간: O(n) — 결과 문자열 크기
#   - 풀이 2: 임시로 2n 길이 생성 후 n으로 슬라이스
#   - 풀이 3: 임시 문자열을 약 n 길이로 줄임 (상수배 절감)
# 결론: n ≤ 10000에선 셋 다 즉시 통과. 차이는 "성능"이 아니라 "표현"의 영역.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. "넉넉히 만들고 잘라내기"는 짝/홀·경계 분기를 없애는 강력한 패턴이다.
#      → 분기가 사라지면 틀릴 코드 자체가 줄어든다.
#   2. 슬라이싱 [:n]은 길이가 n보다 짧아도 에러 없이 있는 만큼만 반환 →
#      "최소 n 이상만 보장"하면 되므로 정확한 반복 횟수 계산이 불필요.
#   3. ⌈n/2⌉(올림 나눗셈)은 (n + 1) // 2로 정수 연산만으로 표현한다.
#   4. 미세 최적화(풀이 3)는 제약을 먼저 보고 판단 — 작은 입력에선 가독성이 우선.

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    (3, "수박수"),
    (4, "수박수박"),
    (1, "수"),
    (2, "수박"),
]

for n, expected in test_cases:
    print(f"입력: n={n}  | 기대값: {expected}")
    print(f"  solution1 → {solution1(n)}")
    print(f"  solution2 → {solution2(n)}")
    print(f"  solution  → {solution(n)}")