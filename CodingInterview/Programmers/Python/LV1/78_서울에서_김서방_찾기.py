# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   문자열 배열 seoul에서 "Kim"이 있는 위치(인덱스)를 찾아
#   "김서방은 N에 있다" 형태로 반환한다.
#   - seoul에 "Kim"은 정확히 한 번만 등장한다(중복·부재 없음).
#   - 예) ["Jane", "Kim"] → "김서방은 1에 있다"

# ─────────────────────────────────────────────────────────────
# 풀이: list.index()로 위치 찾기
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   list.index("Kim") → 리스트에서 "Kim"이 '처음' 나오는 인덱스를 반환한다.
#   - 반환값은 정수라 str()로 변환해 문자열에 이어 붙인다.
#   - 주의: 찾는 값이 없으면 ValueError가 난다(여기선 Kim 존재가 보장되어 안전).
#   - 직접 for문으로 인덱스를 찾을 필요 없이 내장 메서드 한 번이면 된다(O(n)).
def solution(seoul):
    pos = str(seoul.index("Kim"))         # "Kim"의 인덱스(정수) → 문자열로 변환
    return "김서방은 " + pos + "에 있다"

# 더 간결한 대안 (f-string으로 변환·연결을 한 번에):
#   def solution(seoul):
#       return f"김서방은 {seoul.index('Kim')}에 있다"
#   → str() 변환과 + 연결이 사라져 의도가 더 또렷하다.

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 시간: O(n) — index()가 앞에서부터 "Kim"을 찾을 때까지 훑는다(최악 전체).
# 공간: O(1) — 추가 자료구조 없음
# 결론: 한 원소를 선형 탐색하는 게 하한이라 최적 복잡도.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. list.index(x)는 x의 '첫' 등장 인덱스를 돌려준다 — 직접 루프를 짤 필요 없다.
#   2. index()는 값이 없으면 ValueError → 존재가 보장될 때만 안전하게 쓴다.
#   3. 숫자+문자열 연결은 str()+'+' 보다 f"...{값}..." 가 더 읽기 쉽다.

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    (["Jane", "Kim"], "김서방은 1에 있다"),
]

run_tests(solution, test_cases)