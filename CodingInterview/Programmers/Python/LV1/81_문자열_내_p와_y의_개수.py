# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   문자열 s에서 'p'와 'y'의 개수를 세어, 그 수가 같으면 True 아니면 False.
#   - 대소문자는 구분하지 않는다 ('P'와 'p'를 같게 취급)
#   - p, y가 하나도 없으면 0 == 0 이므로 True
#   - 예) "pPoooyY" → p:2, y:2 → True,  "Pyy" → p:1, y:2 → False

# ─────────────────────────────────────────────────────────────
# 풀이 1: 소문자 통일 후 개수 비교
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   lower()로 대소문자를 통일하면 'P'/'p'를 따로 셀 필요가 없다.
#   count()로 'p'와 'y'의 개수를 각각 구해 같은지 비교한 결과를 그대로 반환.
#   p, y가 0개인 경우도 0 == 0 → True 라서 별도 분기가 필요 없다.
def solution(input):
    input = input.lower()
    count_p = input.count("p")
    count_y = input.count("y")
    return count_p == count_y

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 문자열 길이가 L일 때,
# 시간: O(L) — lower() 한 번 + count() 두 번 모두 전체를 훑지만 상수배라 O(L)
# 공간: O(L) — lower()가 소문자로 바꾼 새 문자열을 만든다
# 결론: 문자열을 훑는 게 하한이라 더 줄일 여지는 없는 최적 복잡도.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. 대소문자 무시 비교는 양쪽을 lower()(또는 upper())로 통일하는 게 정석.
#   2. count() == count() 한 줄이면 "0개끼리 같음(True)" 경계까지 자동 처리 →
#      불필요한 if 분기를 만들지 않는 게 더 안전하고 읽기 쉽다.
#   3. 비교 연산 결과(True/False)는 그대로 return 할 수 있다 (if문 불필요).

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    ("pPoooyY", True),
    ("Pyy", False),
    ("", True),       # p, y 둘 다 0개 → 0 == 0
    ("abc", True),    # p, y 없음 → True
    ("ppyy", True),
]

for s, expected in test_cases:
    print(f"입력: {s!r}  | 기대값: {expected}")
    print(f"  solution → {solution(s)}")