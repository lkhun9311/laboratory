# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   두 숫자 문자열 X, Y에서 공통으로 나타나는 숫자(자리)들을 모아
#   만들 수 있는 가장 큰 정수를 문자열로 반환한다.
#   - 짝꿍(공통)이 하나도 없으면 "-1"
#   - 공통 숫자가 0뿐이면 "00..0"이 아니라 "0"
#   - 예) X="100", Y="123450" → 공통 {0,1} → "10"
#   제약: X, Y의 길이가 최대 300만 → int() 변환은 위험(아래 주석 참고)

# ─────────────────────────────────────────────────────────────
# 풀이: 자릿수별 개수 카운팅 + 큰 수부터 조립
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   0~9 각 숫자가 X·Y에 몇 번 나오는지 세고, 둘 중 작은 값(min)이 공통 개수다.
#   9부터 0까지 내림차순으로 (숫자 × 공통개수)를 이어 붙이면 가장 큰 수가 된다.
def solution(X, Y):
    tmp_arr = []
    result = ""

    for i in range(10):
        x_count = X.count(str(i))
        y_count = Y.count(str(i))
        tmp_arr.append(min(x_count, y_count))   # 숫자 i의 공통 개수

    for j in range(9, -1, -1):                  # 큰 숫자부터 붙여 최댓값 구성
        result += str(j)*tmp_arr[j]

    # [엣지 1] 공통 숫자가 없으면 result는 빈 문자열 → "-1"
    #   int(result)로 판단하면 int("")가 ValueError를 내므로, 빈 문자열로 직접 검사.
    if result == "":
        return "-1"

    # [엣지 2] 공통 숫자가 0뿐이면 result는 "000.." → "0" 하나로.
    #   result는 내림차순 조립이라 '가장 큰 숫자'가 맨 앞 → 맨 앞이 "0"이면 전부 0.
    #   주의: int(result) == 0 으로 판단하면 X·Y가 최대 300만 자리라
    #         파이썬의 정수 문자열 변환 한도(기본 4300자리)에 걸려 ValueError 발생!
    #         그래서 변환 없이 result[0]만 확인해 런타임 에러를 피한다.
    if result[0] == "0":
        return "0"

    return result

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 입력 길이가 L(=len(X)+len(Y))일 때,
# 시간: O(L) — count()를 10번 돌지만 각 호출이 O(len), 상수배라 O(L)
# 공간: O(L) — 결과 문자열 크기
# 결론: 자릿수 카운팅은 입력을 훑는 게 하한이라 최적. 정렬(O(L log L)) 불필요.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. "공통 자릿수로 최대 수" → 정렬 대신 0~9 개수 카운팅 + 내림차순 조립이 더 빠르다.
#   2. 거대 입력에서 int(문자열)은 위험 — 파이썬 3.11+는 변환 자릿수 한도(4300)가 있어
#      긴 문자열에 int()를 쓰면 ValueError가 난다. 문자열 상태로 판단하라.
#   3. 빈/0 판정은 int 변환 없이 result == "" 와 result[0] == "0"으로 충분하다.
#      (내림차순 조립이라 맨 앞이 0이면 전체가 0이라는 성질을 활용)

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    (("100", "2345"), "-1"),
    (("100", "203045"), "0"),
    (("100", "123450"), "10"),
    (("12321", "42531"), "321"),
    (("5525", "1255"), "552"),
]

run_tests(solution, test_cases)