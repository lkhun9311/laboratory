# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   가로 길이 n, 세로 길이 m을 입력받아 '*'로 직사각형을 그린다.
#   - 한 줄에 '*'를 n개, 그런 줄을 m개 출력한다.
#   - 예) n=5, m=3 →
#         *****
#         *****
#         *****
#   제약: 1 ≤ n, m ≤ 1000

# ─────────────────────────────────────────────────────────────
# 풀이 1: join으로 줄 잇기
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   "*" * n      → 별 n개짜리 한 줄
#   [... ] * m   → 그 줄을 m개 담은 리스트
#   "\n".join(.) → 줄들 '사이'에만 줄바꿈을 넣어 연결
#   join은 요소 사이에만 구분자를 넣으므로 맨 끝에 불필요한 \n이 안 붙는다.
#   문자열 += 누적(불변 문자열 매번 재생성)의 비효율도 피할 수 있다.

import sys, pathlib
sys.path.append(str(pathlib.Path(__file__).resolve().parent.parent))  # test_runner 경로
from test_runner import run_tests

def solution(n, m):
    return "\n".join(["*" * n] * m)

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 출력 문자 수가 n*m개일 때,
# 시간: O(n*m) — 모든 별을 한 번씩 만들어내는 게 하한
# 공간: O(n*m) — 결과 문자열 크기
# 결론: 직사각형 전체를 만드는 게 목적이라 더 줄일 여지 없는 최적 복잡도.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. "\n".join(리스트)는 줄 단위 출력을 만드는 깔끔한 패턴 →
#      요소 '사이'에만 구분자가 들어가 맨 끝 줄바꿈 문제를 자연히 피한다.
#   2. 같은 줄을 반복할 땐 [x] * m 으로 리스트를 곧장 만들 수 있다.
#   3. 문자열 += 누적은 매번 새 문자열을 생성 → 반복이 많으면 join이 유리하다.
#   4. 안 쓰는 반복 변수는 _ 로 두어 "사용하지 않음"을 드러낸다.

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    ((5, 3), "*****\n*****\n*****"),
    ((1, 1), "*"),
    ((3, 1), "***"),
    ((1, 3), "*\n*\n*"),
]

run_tests(solution, test_cases)

