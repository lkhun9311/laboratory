# ─────────────────────────────────────────────────────────────
# 풀이 1: 같은/다른 개수를 따로 추적
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   x(같은 개수), not_x(다른 개수) 두 카운터를 들고
#   x == not_x가 되는 순간 한 덩어리 확정 → 카운터 초기화
#   루프가 끝난 뒤 균형이 안 맞는 leftover를 따로 +1 처리
def solution1(s):
    result = 0
    init_s = ""                            # 현재 기준 문자
    x = 0                                  # 기준 문자와 같은 글자 수
    not_x = 0                              # 기준 문자와 다른 글자 수

    for el in s:
        if init_s == "":                   # 새 덩어리 시작 → 기준 문자 지정
            init_s = el
            x += 1
            continue
        if el == init_s:
            x += 1
        else:
            not_x += 1

        if x == not_x:                     # 균형 → 한 덩어리 확정
            init_s = ""
            x = 0
            not_x = 0
            result += 1

    if x != not_x:                         # 끝에 남은 미완성 덩어리
        result += 1

    return result


# ─────────────────────────────────────────────────────────────
# 풀이 2: 차이 카운터 하나 + 시작 시점에 카운트 (개선판)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   "같으면 +1, 다르면 -1"인 카운터 cnt 하나로 두 수의 같음을 추적
#   (x == not_x  ⟺  두 수의 차이가 0)
#   cnt == 0인 순간이 곧 "새 덩어리 시작" → 이때 result를 +1
#   시작 시점에 세므로, 끝에 균형이 안 맞는 덩어리도 이미 카운트됨
#   → 별도의 leftover 후처리가 사라진다 (경계 처리 제거)
# 참고: 로직상 base는 cnt == 0일 때 항상 먼저 할당된 뒤 읽히므로 초기화가 없어도
#       동작한다. 다만 린터는 그 흐름을 추적 못 해 "참조 전 할당" 경고를 띄우므로,
#       경고 제거 + 가독성을 위해 명시적으로 ""로 초기화해 둔다. (간결함 vs 안전성 trade-off)
def solution2(s):
    result = 0
    cnt = 0                                # 기준 문자와의 차이 (같으면 +1, 다르면 -1)
    base = ""                              # 현재 기준 문자 (cnt == 0일 때 갱신)

    for el in s:
        if cnt == 0:                       # 새 덩어리 시작
            base = el                      # 기준 문자 지정 (cnt 갱신은 아래 줄에서)
            result += 1                    # 시작하는 순간 미리 +1
        # 같으면 +1, 다르면 -1. 새 덩어리 직후엔 el이 방금 지정한 base와 같으므로
        # 자동으로 +1 → if 안에 따로 cnt += 1을 둘 필요 없이 한 줄로 통합됨
        cnt += 1 if el == base else -1

    return result


# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 시간: 두 풀이 모두 O(n) — 글자를 한 번씩만 훑음
# 공간: O(1) — 부분 문자열을 저장하지 않고 카운터만 사용
# 비교: 풀이 2는 변수 3개 → 2개로 줄고, leftover 후처리도 불필요
# 일반 원리:
#   1) 두 수의 "같음"은 차이를 0과 비교하는 카운터 하나로 줄일 수 있다
#      (괄호 짝 맞추기, 보이어-무어 다수결 투표 등에서 동일 패턴)
#   2) 카운트 시점을 "끝"이 아니라 "시작"으로 옮기면
#      마지막 경계(off-by-one, leftover) 처리가 사라지는 경우가 많다

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. 두 수의 "같음" → 차이 카운터 하나로 축소
#      (x == not_x  ⟺  diff == 0).  변수와 분기가 함께 줄어든다.
#   2. 카운트를 "끝"이 아니라 "시작" 시점에 → leftover 후처리 제거
#      경계 처리는 없애는 게 가장 안전하다(틀릴 코드가 아예 없어짐).
#   3. 여러 분기에서 중복되는 동작은 if 밖으로 빼낸다
#      (cnt += 1 if el == base else -1) — 중복은 버그의 씨앗.
#   4. for문 vs 컴프리헨션 판단 기준:
#      매 반복이 이전 상태(cnt, base)에 의존 → "상태 기계" → 명시적 루프가 정답.
#      독립적 원소 변환일 때만 컴프리헨션/map이 적합.
#   5. 무조건 짧은 코드 ≠ 좋은 코드.
#      reduce로 한 줄에 욱여넣으면 의도(상태 기계)가 가려진다. 명료함 > 짧음.
#   6. 주석과 코드는 항상 같은 말을 해야 한다.
#      base 초기화 여부처럼 "간결함 vs 린터 안전성" 같은 의사결정은 주석에 근거를 남긴다.

