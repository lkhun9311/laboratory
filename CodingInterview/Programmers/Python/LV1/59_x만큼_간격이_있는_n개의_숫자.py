# ─────────────────────────────────────────────────────────────
# 풀이 1: 반복문 + append
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   1부터 n까지 i를 순회하면서 i*x를 결과 리스트에 차례로 append
#   → 가장 직관적인 형태: "x의 1배, 2배, ..., n배"를 순서대로 생성
def solution1(x, n):
    result = []                            # 결과를 담을 빈 리스트
    for i in range(1, n + 1):              # i: 1 ~ n (배수 인덱스)
        result.append(x * i)               # i번째 배수를 추가
    return result


# ─────────────────────────────────────────────────────────────
# 풀이 2: 리스트 컴프리헨션
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   풀이 1과 동일한 로직을 한 줄로 표현
#   → 파이썬에서 "고정 길이 수열 생성" 패턴의 가장 관용적인 표현
def solution2(x, n):
    # range(1, n+1) → 1, 2, ..., n
    # i * x         → i번째 항의 값 (i배수)
    return [i * x for i in range(1, n + 1)]


# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 시간: 두 풀이 모두 O(n) — 원소 n개를 한 번씩 생성하므로 이론적 하한
# 공간: O(n) — 결과 리스트 크기
# 주의: range(x, x*(n+1), x) 방식은 x=0일 때 ValueError 발생
#       → 곱셈 방식(i*x)이 모든 입력값(x=0 포함)에 대해 안전
# 실측: solution2가 리스트 컴프리헨션 최적화 덕분에 상수배 더 빠름

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    ((2, 5),  [2, 4, 6, 8, 10]),
    ((4, 3),  [4, 8, 12]),
    ((-4, 2), [-4, -8]),
]

for (tx, tn), expected in test_cases:
    print(f"입력: x={tx}, n={tn}  | 기대값: {expected}")
    print(f"  solution1 → {solution1(tx, tn)}")
    print(f"  solution2 → {solution2(tx, tn)}")