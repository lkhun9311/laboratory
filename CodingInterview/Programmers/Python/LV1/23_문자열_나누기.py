# ─────────────────────────────────────────────────────────────
#   문자열 s를 앞에서부터 다음 규칙으로 자른다.
#   1. 첫 글자를 기준 문자 x로 정한다.
#   2. x와 같은 글자 수와 다른 글자 수를 센다.
#   3. 두 개수가 같아지는 순간 거기까지 잘라 한 덩어리로 만든다.
#   4. 잘린 다음 글자부터 다시 새 기준 x를 정해 반복.
#   5. 끝까지 균형이 안 맞으면 남은 부분도 한 덩어리로 센다.
#   → 나누어진 부분 문자열의 개수를 반환.

