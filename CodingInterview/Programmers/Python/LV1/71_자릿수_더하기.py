# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   자연수 N을 입력받아 각 자릿수의 합을 반환한다.
#   - 예) N=123 → 1+2+3 = 6,  N=987 → 9+8+7 = 24
#   제약: N은 10000000000 이하의 자연수 → 길이가 작아 성능 부담 없음

# ─────────────────────────────────────────────────────────────
# 풀이 1: 문자열 변환 (파이썬다운 접근)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   N을 문자열로 바꾸면 각 글자가 곧 자릿수가 된다.
#   글자를 하나씩 int로 바꿔 sum으로 한 번에 합산한다.
#   → 누적 변수와 for문 없이 한 줄로 끝나는 가독성 좋은 표현.

import sys, pathlib
sys.path.append(str(pathlib.Path(__file__).resolve().parent.parent))  # test_runner 경로
from test_runner import run_tests

def solution(N):
    return sum(int(i) for i in str(N))


# ─────────────────────────────────────────────────────────────
# 풀이 2: 산술 연산 (보편적 접근)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   % 10으로 마지막 자리를 떼어내고, // 10으로 그 자리를 지워가며 합산한다.
#   문자열 변환 없이 순수 산술로만 처리 → 다른 언어에서도 통용되는 패턴.
#   N == 0이면 while 조건에서 바로 걸러져 result(0)가 그대로 반환된다.
def solution2(N):
    result = 0

    while N > 0:
        result += N % 10  # 마지막 자리 누적
        N = N // 10        # 마지막 자리 제거

    return result

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 자릿수가 d개일 때 (d ≈ log10(N)),
# 시간: 두 풀이 모두 O(d) — 각 자릿수를 한 번씩 처리하는 게 하한
# 공간: 풀이 1은 str(N)로 O(d) 임시 문자열 생성, 풀이 2는 O(1)
# 결론: 제약이 작아 성능 차이는 없음. 선택 기준은 "표현 방식"의 영역.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. 자릿수 분해의 두 갈래: 문자열로 보느냐(str + int), 수로 보느냐(% 10, // 10).
#   2. % 10 / // 10 패턴은 자릿수 뒤집기·진법 변환 등으로 확장되는 기본기다.
#   3. sum(generator)는 "누적 변수 + for문"을 한 줄로 압축하는 파이썬다운 표현.
#   4. while N > 0 조건이 N=0 경계를 자연스럽게 걸러주면서 정답(0)도 보장한다.

# ─────────────────────────────────────────────────────────────
# 테스트
# ─────────────────────────────────────────────────────────────
test_cases = [
    (123, 6),
    (987, 24),
    (1, 1),
    (10, 1),
    (1000, 1),
]

run_tests([solution, solution2], test_cases)