# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   재료가 아래→위로 쌓일 때(1=빵, 2=야채, 3=고기), 연속으로
#   빵-야채-고기-빵 = [1, 2, 3, 1] 4개가 만들어지면 햄버거 1개로 포장(제거)한다.
#   - 제거하면 그 위/아래가 맞붙어 또 햄버거가 될 수 있다.
#   - 총 포장 개수를 반환한다.
#   - 예) [2,1,1,2,3,1,2,3,1] → 2개
#   제약: 길이 최대 1,000,000 → 매번 전체 재스캔(O(n^2))은 시간 초과. O(n) 필요.

# ─────────────────────────────────────────────────────────────
# 풀이: 스택 + 맨 위 4개 검사
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   재료를 스택에 하나씩 쌓고, 쌓을 때마다 '맨 위 4개'만 본다.
#   [1,2,3,1]이면 4개를 빼고(포장) 카운트 → 빼면 스택의 새 맨 위가 직전 재료가 되어
#   "위아래가 맞붙는" 경우가 자동으로 처리된다(다음 push 때 다시 검사됨).
#   각 재료는 push 1번·pop 최대 1번뿐이라 전체 O(n).
def solution(ingredient):
    stack = []
    result = 0

    for i in ingredient:
        stack.append(i)
        # 패턴은 닫는 빵(1)으로 끝나므로, i가 1일 때만 검사해 불필요한 비교를 줄인다.
        if len(stack) >= 4 and i == 1:
            if stack[-4:] == [1, 2, 3, 1]:   # 맨 위 4개가 햄버거면
                result += 1
                del stack[-4:]               # 4개 제거(포장)

    return result

# 더 나은 방법(메모리 O(1)) — 별도 스택 없이 입력 배열을 버퍼로 재사용 + 포인터:
