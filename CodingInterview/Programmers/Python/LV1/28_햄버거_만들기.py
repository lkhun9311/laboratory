#           ingredient[top] = x              # 입력을 덮어쓰며 스택처럼 사용
#           top += 1
#           if top >= 4 and ingredient[top-4:top] == [1, 2, 3, 1]:
#               top -= 4                      # '제거'는 포인터만 되돌림
#               count += 1
#       return count
#   → 시간은 동일 O(n), 추가 공간 O(1). 단 입력 배열을 변형하는 부작용이 있다.

