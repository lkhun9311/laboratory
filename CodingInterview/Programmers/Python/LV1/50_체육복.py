# ─────────────────────────────────────────────────────────────
# 문제 규칙
# ─────────────────────────────────────────────────────────────
#   체육복을 도난당한 학생(lost)에게 여벌 보유 학생(reserve)이 빌려줄 때,
#   체육 수업을 들을 수 있는 학생의 최댓값을 구한다.
#   - 여벌은 자기 바로 앞/뒤 번호(±1)에게만 빌려줄 수 있다.
#   - 핵심 함정: 여벌이 있지만 자기 것도 도난당한 학생(lost ∩ reserve)은
#     자기 여벌을 입어야 하므로 남에게 빌려줄 수 없다.
#   제약: 전체 학생 수 n ≤ 30

# ─────────────────────────────────────────────────────────────
# 풀이: 차집합으로 함정 제거 + 그리디(왼쪽 먼저)
# ─────────────────────────────────────────────────────────────
# 핵심 아이디어:
#   set(lost) - set(reserve) → 여벌 없이 정말 빌려야 하는 학생(need)
#   set(reserve) - set(lost) → 빌려줄 수 있는 학생(give)
#     → 두 차집합이 "도난당했지만 여벌도 있는 학생"을 양쪽에서 걸러낸다.
#   give를 '낮은 번호부터' 돌며 g-1(왼쪽) 먼저, 없으면 g+1(오른쪽)에게 빌려준다.
#     → 왼쪽 우선 그리디가 최적해를 보장한다(낮은 번호를 먼저 해결).
#   need가 set이라 멤버십 확인·삭제가 O(1) → 리스트(O(n))보다 빠르다.
def solution(n, lost, reserve):
    need = set(lost) - set(reserve)   # 진짜 빌려야 하는 학생
    give = set(reserve) - set(lost)   # 빌려줄 수 있는 학생

    for g in sorted(give):            # 낮은 번호부터
        if g-1 in need:               # 왼쪽 이웃 먼저
            need.remove(g-1)
        elif g+1 in need:             # 없으면 오른쪽 이웃
            need.remove(g+1)

    return n - len(need)              # 전체 - 끝내 못 빌린 학생

# ─────────────────────────────────────────────────────────────
# 복잡도 분석
# ─────────────────────────────────────────────────────────────
# 시간: O(n log n) — sorted(give)가 지배적. 루프 내 set 연산은 O(1).
#   (need를 리스트로 두면 in/remove가 O(n)이라 전체 O(n^2)이 됨)
# 공간: O(n) — need, give 집합 크기
# 더 줄이려면: 길이 n+2 배열에 +1/-1로 카운팅하면 정렬 없이 O(n)도 가능.

# ─────────────────────────────────────────────────────────────
# 이 문제에서 배운 것 (Takeaways)
# ─────────────────────────────────────────────────────────────
#   1. "도난∩여벌" 같은 엣지 케이스는 차집합 두 번으로 양쪽에서 동시에 제거된다.
#   2. 그리디는 순서가 곧 정당성 — '낮은 번호부터 + 왼쪽 우선'이 최적을 보장한다.
#   3. 멤버십·삭제가 잦으면 list(O(n)) 대신 set(O(1)) → O(n^2)을 O(n log n)으로.
#   4. 순회 순서가 필요한 컬렉션(give)만 정렬하고, 조회만 하는 컬렉션(need)은 set.

# ─────────────────────────────────────────────────────────────
# 테스트
